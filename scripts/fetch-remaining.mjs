async function sleep(ms) { return new Promise(r => setTimeout(r, ms)); }

async function apiFetch(path) {
  const res = await fetch(`${BASE}${path}`, { headers: HEADERS });
  const json = await res.json();
  if (json.errors && Object.keys(json.errors).length > 0) throw new Error(JSON.stringify(json.errors));
  return json;
}

async function main() {
  const statusJson = await apiFetch("/status");
  const { current, limit_day } = statusJson.response.requests;
  const remaining = limit_day - current;
  const needed = Object.keys(REMAINING).length + 1; // +1 for status call
  console.log(`Quota: ${current}/${limit_day} used, ${remaining} left. Need ~${needed}.`);
  if (remaining < needed) {
    console.error(`Not enough quota today (${remaining} < ${needed}). Run again tomorrow.`);
    process.exit(1);
  }

  // Load existing player data from generated file
  const existingPath = join(root, "src", "playerPhotos.ts");
  const existing = readFileSync(existingPath, "utf8");
  // Extract existing entries
  const playerMap = {};
  for (const m of existing.matchAll(/^\s+"([^"]+)":\s+"([^"]+)"/gm)) {
    playerMap[m[1]] = m[2];
  }
  console.log(`Loaded ${Object.keys(playerMap).length} existing players`);

  let fetched = 0;
  const total = Object.keys(REMAINING).length;

  for (const [teamId, teamName] of Object.entries(REMAINING)) {
    process.stdout.write(`  [${++fetched}/${total}] ${teamName} (${teamId})… `);
    try {
      const json = await apiFetch(`/players/squads?team=${teamId}`);
      let count = 0;
      for (const block of json.response ?? []) {
        for (const p of block.players ?? []) {
          if (p.name && p.photo && !playerMap[p.name]) {
            playerMap[p.name] = p.photo;
            count++;
          }
        }
      }
      console.log(`+${count} new players`);
    } catch (e) {
      console.log(`FAILED: ${e.message}`);
    }
    await sleep(2000); // 1 req/2s
  }

  // Rewrite playerPhotos.ts
  const entries = Object.entries(playerMap).sort(([a], [b]) => a.localeCompare(b));
  const lines = [
    "// Auto-generated by scripts/fetch-player-ids.mjs — do not edit manually",
    `// Generated: ${new Date().toISOString()} · ${entries.length} players`,
    "",
    "const PLAYER_PHOTOS: Record<string, string> = {",
    ...entries.map(([name, url]) => `  ${JSON.stringify(name)}: ${JSON.stringify(url)},`),
    "};",
    "",
    "// Build a last-name index at module load for fast lookup",
    "const BY_LAST: Record<string, string> = {};",
    "for (const [k, v] of Object.entries(PLAYER_PHOTOS)) {",
    '  const last = k.split(" ").slice(-1)[0].toLowerCase();',
    "  if (!BY_LAST[last]) BY_LAST[last] = v;",
    "}",
    "",
    "export function playerPhoto(name: string): string | undefined {",
    "  if (PLAYER_PHOTOS[name]) return PLAYER_PHOTOS[name];",
    '  const lastName = name.split(" ").slice(-1)[0].toLowerCase();',
    "  return BY_LAST[lastName];",
    "}",
  ];
  writeFileSync(existingPath, lines.join("\n") + "\n");
  console.log(`\nWrote ${existingPath} — ${entries.length} total players`);
}

main().catch(e => { console.error(e); process.exit(1); });
